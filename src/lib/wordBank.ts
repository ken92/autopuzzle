export const BANK_LABELS = Object.keys(BANKS_AND_LABELS) as WordBankKey[];;

export type WordBankKey = keyof typeof BANKS_AND_LABELS;

/**
 * Return N random words chosen from the given banks.
 * - Combines the selected banks, deduplicates entries, shuffles, and returns the requested count.
 * - If numberOfWords >= available unique words, returns all unique words in random order.
 */
export function getRandomWordsFromBanks(bankKeys: WordBankKey[], numberOfWords: number): string[] {
  if (!bankKeys || bankKeys.length === 0 || numberOfWords <= 0) return [];

  // gather words from the requested banks
  const pool: string[] = [];
  for (const k of bankKeys) {
    const list = BANKS_AND_LABELS[k] as readonly string[] | undefined;
    if (list && list.length) {
      pool.push(...list);
    }
  }

  if (pool.length === 0) return [];

  // dedupe
  const unique = Array.from(new Set(pool));

  // Fisher-Yates shuffle
  for (let i = unique.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const tmp = unique[i];
    unique[i] = unique[j];
    unique[j] = tmp;
  }

  if (numberOfWords >= unique.length) return unique;
  return unique.slice(0, numberOfWords);
}
